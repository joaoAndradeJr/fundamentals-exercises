function sum(a, b) {
  return a + b;
}

// 2
function sub(a, b) {
  return a - b;
}

// 3

function mul(a, b) {
  return a * b;
}

// 4
function div(a, b) {
  if (a <= 0) {
    return "Não pode dividir por 0";
  } else {
    return a / b;
  }
}

// 5
function mod(a, b) {
  if (a <= 0) {
    return "Não pode dividir por 0";
  } else {
    return a % b;
  }
}
